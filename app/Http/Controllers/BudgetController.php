     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $budjet = new Budget();
        $budjet->project_id = $request->input('project_id');
        $budjet->service_id = $request->input('service_id');
        $budjet->qty = $request->input('qty');
        $budjet->price = $request->input('price');
        $budjet->percentage = 0;
        $budjet->save();

        return redirect()->back()->with('success', 'La ville a été ajouter avec succès !');
    }

